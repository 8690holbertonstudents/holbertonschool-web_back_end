#!/usr/bin/env python3
"""
Module to use typed variables
"""


def concat(str1: str, str2: str) -> str:
    """
    Function to concatenate 2 strings
    Args:
        str1 (str): first string
        str2 (str): second string
    Returns:
        str: the concatenation of str1 and str2
    """
    return str1 + str2
